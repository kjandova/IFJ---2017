#include <stdio.h>
void print_builtins(FILE *f) {
fputs(
"LABEL &min\n"
"CREATEFRAME\n"
"PUSHFRAME\n"
"DEFVAR LF@x\n"
"DEFVAR LF@y\n"
"DEFVAR LF@acc\n"
"DEFVAR LF@ret\n"
"POPS LF@x\n"
"POPS LF@y\n"
"GT LF@ret LF@x LF@y\n"
"JUMPIFEQ &min%b1 LF@ret bool@true\n"
"MOVE LF@ret LF@x\n"
"JUMP &min%end\n"
"LABEL &min%b1\n"
"MOVE LF@ret LF@y\n"
"LABEL &min%end\n"
"PUSHS LF@ret\n"
"POPFRAME\n"
"RETURN\n"
"LABEL &max\n"
"CREATEFRAME\n"
"PUSHFRAME\n"
"DEFVAR LF@x\n"
"DEFVAR LF@y\n"
"DEFVAR LF@ret\n"
"POPS LF@x\n"
"POPS LF@y\n"
"LT LF@ret LF@x LF@y\n"
"JUMPIFEQ &max%b1 LF@ret bool@true\n"
"MOVE LF@ret LF@x\n"
"JUMP &max%end\n"
"LABEL &max%b1\n"
"MOVE LF@ret LF@y\n"
"LABEL &max%end\n"
"PUSHS LF@ret\n"
"POPFRAME\n"
"RETURN\n"
"LABEL &asc\n"
"CREATEFRAME\n"
"PUSHFRAME\n"
"DEFVAR LF@s\n"
"DEFVAR LF@i\n"
"DEFVAR LF@ret\n"
"POPS LF@s\n"
"POPS LF@i\n"
"SUB LF@i LF@i int@1\n"
"PUSHS LF@i\n"
"PUSHS int@0\n"
"LTS\n"
"STRLEN LF@ret LF@s\n"
"PUSHS LF@ret\n"
"PUSHS LF@i\n"
"LTS\n"
"ORS\n"
"POPS LF@ret\n"
"JUMPIFEQ &asc%b1 LF@ret bool@true\n"
"STRI2INT LF@ret LF@s LF@i\n"
"JUMP &asc%end\n"
"LABEL &asc%b1\n"
"MOVE LF@ret int@0\n"
"LABEL &asc%end\n"
"PUSHS LF@ret\n"
"POPFRAME\n"
"RETURN\n"
"LABEL &substr\n"
"CREATEFRAME\n"
"PUSHFRAME\n"
"DEFVAR LF@s\n"
"DEFVAR LF@i\n"
"DEFVAR LF@n\n"
"DEFVAR LF@acc\n"
"DEFVAR LF@ret\n"
"POPS LF@s\n"
"POPS LF@i\n"
"POPS LF@n\n"
"MOVE LF@ret string@\n"
"SUB LF@i LF@i int@1\n"
"LT LF@acc LF@i int@0\n"
"JUMPIFEQ &substr%end LF@acc bool@true\n"
"STRLEN LF@acc LF@s\n"
"SUB LF@acc LF@acc LF@i\n"
"PUSHS LF@n\n"
"PUSHS LF@acc\n"
"PUSHS int@0\n"
"CALL &max\n"
"CALL &min\n"
"POPS LF@n\n"
"LABEL &substr%l1\n"
"JUMPIFEQ &substr%end LF@n int@0\n"
"GETCHAR LF@acc LF@s LF@i\n"
"CONCAT LF@ret LF@ret LF@acc\n"
"ADD LF@i LF@i int@1\n"
"SUB LF@n LF@n int@1\n"
"JUMP &substr%l1\n"
"LABEL &substr%end\n"
"PUSHS LF@ret\n"
"POPFRAME\n"
"RETURN\n"
, f);
}
